// State
let currentLang = 'en';
let currentDungeon = null;
let failCount = 0;

// Elements
const dungeonNav = document.getElementById('dungeon-nav');
const bossContainer = document.getElementById('boss-container');
const langBtn = document.getElementById('lang-btn');
const failCountEl = document.getElementById('fail-count');
const failQuoteEl = document.getElementById('fail-quote');
const titleEl = document.getElementById('title');
const subtitleEl = document.getElementById('subtitle');
const failLabelEl = document.getElementById('fail-label');
const footerLaunchEl = document.getElementById('footer-launch');

// Init
function init() {
    // Load fails
    const savedFails = localStorage.getItem('davidFails');
    if (savedFails) {
        failCount = parseInt(savedFails);
        failCountEl.textContent = failCount;
    }

    // Generate Nav
    const dungeonNames = Object.keys(translations.en.dungeons);
    dungeonNames.forEach(name => {
        const btn = document.createElement('button');
        btn.className = 'nav-btn';
        btn.textContent = name;
        btn.onclick = () => selectDungeon(name);
        dungeonNav.appendChild(btn);
    });

    updateUI();
    
    langBtn.onclick = () => {
        currentLang = currentLang === 'en' ? 'de' : 'en';
        updateUI();
        if (currentDungeon) renderBosses();
    };
}

function selectDungeon(name) {
    currentDungeon = name;
    
    // Update active state
    document.querySelectorAll('.nav-btn').forEach(btn => {
        if (btn.textContent === name) btn.classList.add('active');
        else btn.classList.remove('active');
    });

    renderBosses();
}

function renderBosses() {
    const t = translations[currentLang];
    const bosses = t.dungeons[currentDungeon];
    const meta = DUNGEON_METADATA[currentDungeon];

    bossContainer.innerHTML = '';
    
    bosses.forEach(boss => {
        const bossMeta = meta?.bosses?.[boss.name];
        const bossImg = bossMeta?.img || "https://wow.zamimg.com/images/wow/icons/large/inv_misc_questionmark.jpg";
        const bossLink = bossMeta?.link || meta?.link || "#";

        const card = document.createElement('article');
        card.className = 'boss-card';
        
        const tipsHtml = boss.tips.map(tip => `<li>${tip}</li>`).join('');

        card.innerHTML = `
            <div class="boss-card-sidebar">
                <img src="${bossImg}" alt="${boss.name}" class="boss-img-round" 
                     onerror="this.src='https://wow.zamimg.com/images/wow/icons/large/inv_misc_questionmark.jpg'">
            </div>
            <div class="boss-content">
                <h3>${boss.name}</h3>
                <ul class="tips-list">
                    ${tipsHtml}
                </ul>
                <a href="${bossLink}" target="_blank" class="guide-link">
                    ${t.fullGuide}
                </a>
            </div>
        `;
        bossContainer.appendChild(card);
    });
}

function updateUI() {
    const t = translations[currentLang];
    titleEl.textContent = t.title;
    subtitleEl.textContent = t.subtitle;
    failLabelEl.textContent = t.failLabel;
    footerLaunchEl.textContent = t.launch;
    langBtn.textContent = currentLang === 'en' ? 'DE' : 'EN';
    
    if (!currentDungeon) {
        bossContainer.innerHTML = `<div class="text-center py-10 opacity-60">${t.selectDungeon}</div>`;
    }

    // Update quote
    const quotes = t.failQuotes;
    failQuoteEl.textContent = `"${quotes[0]}"`;
}

function changeFails(amount) {
    failCount = Math.max(0, failCount + amount);
    failCountEl.textContent = failCount;
    localStorage.setItem('davidFails', failCount);

    if (amount > 0) {
        const quotes = translations[currentLang].failQuotes;
        const randomQuote = quotes[Math.floor(Math.random() * quotes.length)];
        failQuoteEl.textContent = `"${randomQuote}"`;
    }
}

init();
